local lush = require('lush')
local P = require('palette')
local Red = P.Red
local Orange = P.Orange
local Yellow = P.Yellow
local Green = P.Green
local Mint = P.Mint
local Blue = P.Blue
local DarkBlue = P.DarkBlue
local Purple = P.Purple
local BlueGrey = P.BlueGrey
local Grey = P.Grey
local base = require('lush_theme.yacolors')

-- LSP/Linters mistakenly show `undefined global` errors in the spec, they may
-- support an annotation like the following. Consult your server documentation.
---@diagnostic disable: undefined-global
local theme = lush(function()
  return {
    Normal { bg = BlueGrey[20], fg = BlueGrey[1] }, -- Normal text
  }
end)

return lush.merge({ base, theme })

